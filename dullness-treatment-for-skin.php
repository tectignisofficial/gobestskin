                compSlider.each(function () {
                    let compSliderWidth = $(this).width() + "px";
                    $(this).find(".resize img").css({
                        width: compSliderWidth
                    });
                    drags($(this).find(".divider"), $(this).find(".resize"), $(this));
                });
                $(window).on("resize", function () {
