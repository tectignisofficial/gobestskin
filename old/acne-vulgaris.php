            of natural oil or sebum combined with the buildup of dead cells and bacteria leads to the clogging of the
            hair follicles. It results in inflammation and redness in the form of acne.</p>
          <p>Facial acne is one of the most widespread skin concern that includes breakouts on the forehead, nose,
            cheek, chin, jawline and even neck. However, pimples may also appear on other areas of the body, including
            chest, shoulders, arms and back. Acne usually occurs during the teenage phase due to puberty. However, for
            some of us, pimples may persist or become more severe during adulthood. Let us find out more about the
            types, causes and symptoms of acne vulgaris before exploring preventive tips and treatment options.</p>
